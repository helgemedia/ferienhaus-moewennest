<p>
  Liebe Nordseeurlauber und Hundehalter,
  <br>
  wir möchten Ihnen gerne unser Ferienhaus im Cuxland Ferienpark in Dorum-Neufeld an der Wurster Nordseeküste vorstellen.
</p>
<p>
  Unser Haus "Möwennest“ wurde von uns mit viel Liebe hochwertig und praxistauglich eingerichtet.
  Wir haben hier einen Rückzugsort geschaffen, den wir auch selbst gerne nutzen.
  Unser 62 qm großes Ferienhaus wurde im Juni 2018 fertiggestellt.
  Es hat zwei Schlafzimmer, ein Badezimmer und eine offene Wohnküche, die mit allem ausgestattet ist was einen angenehmen Urlaub gewährleistet.
  In jedem Schlafzimmer, sowie im Wohnzimmer befindet sich ein Fernseher.
  Die Küche ist mit einem Backofen, einem Ceran-Kochfeld, einer Mikrowelle, Toaster, Kaffeemaschine sowie einer Spülmaschine versehen.
  In der Küche ist außerdem eine Kühl- Gefrierkombination mit großem Kühlschrank und 3 Gefrierschubladen.
</p>
<p>
  Die große Dusche mit Regendusche macht das Badezimmer zu einer Wellness Oase.
  Für Ihre Wäsche ist eine Waschmaschine, ein Bügelbrett und Bügeleisen vorhanden. Für Ihren Hund haben wir Hundenäpfe und ein Hundekörbchen (geeignet für mittelgroße Hunde).
  Im Wohnzimmer gibt es Bücher und Spiele für Erwachsene und Kinder sowie Hörbücher. Sollte das Wetter einmal nicht so gut sein, backen oder kochen sie doch etwas Schönes mit Ihrem Schatz und Ihren Kindern.
  Im Abstellraum finden Sie übrigens ein Waffeleisen. Eine Waffel mit Vanilleeis und einer leckeren Tasse Tee schmeckt immer und versüßt einen verregneten Nachmittag.
  Meist ist aber Regen an der Nordsee so schnell verschwunden wie er gekommen ist.
  An allen Fenstern befinden sich außen elektrische Rollos zur Verdunklung der Räume.
</p>
<p>
  Im Außenbereich befinden sich zwei Terrassen, so dass Sie die Sonne zu jeder Tageszeit nutzen können.
  Ein Tisch mit 6 Stühlen sowie zwei Sonnenliegen machen das Ganze komplett.
  Der Strand und der Hundestrand befinden sich in etwa 900 m Entfernung und sind zu Fuß in etwa 10 Minuten zu erreichen.
  In unmittelbarer Nähe unseres Ferienhauses ist ein Spielplatz, weitere große und sehr schöne Spielplätze sind am Strand. Dorum Neufeld hat außerdem für Regenwetter ein Kinderspielhaus.  Das Bad „Watt’n Bad“ mit Freibad (Wellenbad alle halbe Stunde) und Hallenbad ist eine weitere Attraktion vor Ort. In etwa 25 km Entfernung finden Sie Cuxhaven und Bremerhaven mit weiteren Freizeitmöglichkeiten, wobei ich Ihnen besonders den „Zoo am Meer“ in Bremerhaven ans Herz legen möchte. Hundebesitzer können den Agility Platz im Ferienpark nutzen.
  Den Schlüssel bekommen Sie im Hotel.
  Einkaufsmöglichkeiten gibt es in etwa 500 m Entfernung.
  Für größere Einkäufe bietet sich Dorum in etwa 5 km Entfernung an.
  Hier gibt es Lidl, Aldi, Netto und zwei Edeka Märkte.
</p>
<p>
  Schicken sie uns gerne eine persönliche Nachricht.
</p>
